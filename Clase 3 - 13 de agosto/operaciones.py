# suma 
# + 
# 5 + 3 
resultado = 5 + 3
print(f"suma {resultado}")
print("suma " + str(resultado)) # concatenación de string
# resta 
# - 
# 5 - 3
resultado = 5 - 3
print(f"resta {resultado}")
# multiplicación
# *
# 5 * 3 
resultado = 5 * 3
print(f"multiplicación {resultado}")
# potencia 
# ** 
# 5 ** 3 
resultado = 5 ** 3
print(f"potencia {resultado}")
# radicación 
resultado = 4 ** (1/2) # => raiz cuadrada de 4 (√4)
print(f"radicación {resultado}")
# división (cociente)
# / 
# 5.0 / 3.0
resultado = 5 / 3
print(f"división (cociente) {resultado:.2f}")
# división (parte entera)
# // 
# 5.0 // 3.0
resultado = 5 // 3
print(f"división (parte entera) {resultado}")
# división (resto)
# % 
# 5 % 3
resultado = 5 % 3
print(f"división (resto) {resultado}")